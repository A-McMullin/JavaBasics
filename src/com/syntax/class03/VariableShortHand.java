package com.syntax.class03;

public class VariableShortHand {

	public static void main(String[] args) {
		
		int num=100;
		
		num-=60;
		System.out.println(num);
		
		int cakepiece=11;
		
		cakepiece/=4;
		System.out.println(cakepiece);
		
		int cake = 25;
		
		cake%=7;
		System.out.println(cake);
		
		
		
	}

}
