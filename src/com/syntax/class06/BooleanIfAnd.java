package com.syntax.class06;

public class BooleanIfAnd {

	public static void main(String[] args) {
		
		boolean snow=false;
		boolean sunny=true;
		
		if (snow && sunny) {
			System.out.println("I will go outside");
			
		} else {
			System.out.println("I will stay inside");
		}

		boolean cold=false;
		
		if (!cold) {
			System.out.println("Enjoy the day");
		
		}
	}

}
