 package com.syntax.class02;

public class ArithmaticTasks {

	public static void main(String[] args) {
		
			double a=100.56;
			double b=6.84;
			
			double sum=a+b;
			double sub=a-b;
			double mul=a*b;
			double div=a/b;
			
			
			System.out.println("The addition of 2 numbers, "+a+" and "+b+", is equal to "+sum);
			System.out.println("The subtraction of 2 numbers, "+a+" and "+b+", is equal to "+sub);
			System.out.println("The multiplication of 2 numbers, "+a+" and "+b+", is equal to "+mul);
			System.out.println("The division of 2 numbers, "+a+" and "+b+", is equal to "+div);
			
			double e=3.9;
			double square=e*e;
			
			System.out.println("The square of "+e+" is "+square);
			
			int width=5;
			int height=8;
			
			int perimeter=2*width+2*height;			
			int area=width*height;
			
						
			System.out.println("The perimeter of a rectangle with width "+width+" and height "+height+" is equal to "+perimeter+
					" and the area is equal to "+area);
			
			
			

	}

}
