package com.syntax.class03;

public class ShortHandOperators {

	public static void main(String[] args) {
		
		int num=100;
		
		num=num + 100;
				
		System.out.println(num);

		num+=100;
		System.out.println(num); //200 + 100 = 300
		
		num-=20;
		System.out.println(num); //300 - 20 = 280
		
		num*=4;
		System.out.println(num); //280 * 4 = 1120
		
		num/=10;
		System.out.println(num); //1120 / 10 = 112
		
		
		
		
	}

}
