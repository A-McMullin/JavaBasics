package com.syntax.class15;

public class StringMethodsCont {

	public static void main(String[] args) {
		
		String name = "222-444-5555";
		System.out.println(name.substring(8)); // prints the string from a specified starting char number
		System.out.println(name.substring(3)); // another example
		System.out.println(name.substring(3,7)); // prints string from a start to end point
		
	}

}
