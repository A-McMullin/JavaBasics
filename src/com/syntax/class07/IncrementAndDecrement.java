package com.syntax.class07;

public class IncrementAndDecrement {

	public static void main(String[] args) {
		
		// Increment Operator
		int num=10;
		
		num=num+1; //adds 1 to variable
		
		num+=1; //adds 1 to variable
		
		num++; //adds 1 to variable
		
				
		System.out.println(num);
		
		
		//Decrement Operator
		
		int num1=10; 
		
		num1=num1-1; //subtracts 1 from variable
		
		num1-=1; //subtracts 1 from variable
		
		num1--; //subtracts 1 from variable
		
		System.out.println(num1);
	}

}
